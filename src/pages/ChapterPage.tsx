import React, { useEffect, useState } from 'react';















import { Link, useParams } from 'react-router-dom';















import { fetchChapterVersesWithTranslation } from '../api';































interface Verse {















  text_uthmani: string;















  verse_key: string;















  translations: Array<{ text: string }>;















}































const ChapterPage: React.FC = () => {















  const [verses, setVerses] = useState<Verse[]>([]);















  const [loading, setLoading] = useState(true);















  const [error, setError] = useState<string | null>(null);















  const { chapterId } = useParams<{ chapterId: string }>();































  useEffect(() => {















    const loadVerses = async () => {















      if (chapterId) {















        try {















          const fetchedVerses = await fetchChapterVersesWithTranslation(Number(chapterId));















          setVerses(fetchedVerses);















          setLoading(false);















        } catch (err) {















          setError('Failed to load verses. Please try again later.');















          setLoading(false);















        }















      }















    };































    loadVerses();















  }, [chapterId]);































  if (loading) return <div>Loading...</div>;















  if (error) return <div>{error}</div>;















  if (!chapterId) return <div>Chapter not found</div>;































  return (















    <div className="chapter-page">















      <h1>Surah {chapterId}</h1>















      <div className="verse-list">















        {verses.map((verse) => (















          <div key={verse.verse_key} className="verse-item">















            <Link to={`/verse/${chapterId}/${verse.verse_key.split(':')[1]}`}>















              <p dir="rtl" lang="ar" className="arabic-text">{verse.text_uthmani}</p>















              <p className="translation-text">{verse.translations[0].text}</p>















            </Link>















          </div>















        ))}















      </div>















      <Link to="/" className="back-link">Back to Surahs</Link>















    </div>















  );















};































export default ChapterPage;






































